public class SwitchStatementTutor {
    public static void main(String[] args) {
        int a = 3;
        switch (a){
            case 1:
                System.out.println("Match");
                break;
            case 2:
                System.out.println("Not Match");
                break;
            case 3:
                System.out.println("Match");
                break;
            default:
                System.out.println("Nothing");
                break;
        }
    }
}
