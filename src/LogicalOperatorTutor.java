public class LogicalOperatorTutor {
    public static void main(String[] args) {
        int a =10;
        int b = 11;
        int c =19;
        int d = 20;
        System.out.println(a>b && c>d); //AND
        System.out.println(a<b || c<d); //OR
        System.out.println(!(a<b));  // NOT

    }
}
