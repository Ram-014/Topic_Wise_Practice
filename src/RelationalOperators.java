public class RelationalOperators {
    public static void main(String[] args) {
        int a =5; // here we used the '=' to assign value of 5 to 'a'
        System.out.println(a);
        int x = 10;
        int y = 11;
        boolean result = x<y;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(result);
    }
}
