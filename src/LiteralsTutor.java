public class LiteralsTutor {

    public static void main(String[] args) {
        int a =10;
        int b =220;
        double c = 12.5;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        //Here The Values Like 10,,220,12.5 are literals which means values are known as literals
    }
}
